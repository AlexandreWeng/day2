function changebackground() {
  var magicNumber1 = 1;
  let magicNumber2 = 2;
  let magicNumber3 = 3;

  if (magicNumber1 === 1) {
    paintYellowRectangle();
  }
  if (magicNumber2 === 2) {
    paintGreenRectangle();
  }
  if (magicNumber3 === 3) {
    paintRedRectangle();
  }
}

changebackground();
